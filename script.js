/* =============== LANGUAGE SWITCHING =============== */
let currentLang = localStorage.getItem('lang') || 'en';

const langFlags = {
    en: 'üá∫üá∏',
    es: 'üá™üá∏',
    fr: 'üá´üá∑'
};

const langCodes = {
    en: 'EN',
    es: 'ES',
    fr: 'FR'
};

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
    
    // Update dropdown button to show selected language
    const dropdownBtn = document.getElementById('lang-dropdown-btn');
    if (dropdownBtn) {
        dropdownBtn.querySelector('.lang-flag').textContent = langFlags[lang];
        dropdownBtn.querySelector('.lang-code').textContent = langCodes[lang];
    }
    
    // Update active state on dropdown options
    document.querySelectorAll('.lang-option').forEach(option => {
        option.classList.remove('active');
        option.setAttribute('aria-selected', 'false');
        if (option.getAttribute('data-lang') === lang) {
            option.classList.add('active');
            option.setAttribute('aria-selected', 'true');
        }
    });
    
    // Update html lang attribute
    document.documentElement.lang = lang;
}

function toggleDropdown() {
    const dropdown = document.querySelector('.lang-dropdown');
    const btn = document.getElementById('lang-dropdown-btn');
    dropdown.classList.toggle('open');
    btn.setAttribute('aria-expanded', dropdown.classList.contains('open'));
}

function closeDropdown() {
    const dropdown = document.querySelector('.lang-dropdown');
    const btn = document.getElementById('lang-dropdown-btn');
    if (dropdown) {
        dropdown.classList.remove('open');
        btn.setAttribute('aria-expanded', 'false');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
    
    // Toggle dropdown on button click
    const dropdownBtn = document.getElementById('lang-dropdown-btn');
    if (dropdownBtn) {
        dropdownBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            toggleDropdown();
        });
    }
    
    // Add click listeners to language options
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', () => {
            const lang = option.getAttribute('data-lang');
            setLanguage(lang);
            closeDropdown();
        });
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.lang-dropdown')) {
            closeDropdown();
        }
    });
});

